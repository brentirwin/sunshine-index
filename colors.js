export { colorMap };

const colorMap = num => {
  const colors = [  [204, 230, 204],  // 0
                    [204, 230, 204],  // 1
                    [204, 230, 0],    // 2
                    [255, 255, 0],    // 3
                    [255, 204, 0],    // 4
                    [255, 153, 0],    // 5
                    [255, 102, 0],    // 6
                    [255, 0, 0],      // 7
                    [204, 0, 0],      // 8
                    [153, 0, 0],      // 9
                    [102, 0, 0],      // 10
                    [102, 0, 102],    // 11
                    [153, 0, 153]     // 12
                 ];
  return 'rgb(' + colors[num].join() + ')';
}
